package it.java.programming.beginner.to.advanced;

public class ManageStrings {

    public static void main(String[] args) {
        int firstValue = 9;
        double secondValue = 9.5;
        String text = "Hi";
        String blankText = " ";
        String moreText = "there";
        System.out.println(text);
        System.out.println(text + blankText + moreText);
        System.out.println("Hi" + " " + "there");
        System.out.println("My values are: " + firstValue + ", " + secondValue);
    }
}
